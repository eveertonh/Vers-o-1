idade = int(input("Digite a sua Idade:> "))

if idade == 18:
    print("O Aluno tem 18")

elif idade >= 18:
    print("O Aluno é maior ou igual a 18")

elif idade <= 18:
    print("O Aluno é menor ou igual a 18")

elif idade > 18:
    print("O Aluno é maior que 18")

elif idade < 18:
    print("O Aluno é menor que 18")

elif idade != 18:
    print("O Aluno é diferente que 18")

else:
    print("Adios Muchacho")